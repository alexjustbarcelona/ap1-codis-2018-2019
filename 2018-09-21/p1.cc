
/* Algunes capçaleres de funcions i la seva documentació. */


// mcd(x, y) retorna el màxim comú dividor de x i de y.
// Precondició: x, y ≥ 0 i x + y > 0.

int mcd(int x, int y);



// factorial(n) retorna n!, és a dir, el factorial de n,
// que val n × (n-1) × (n-2) × ··· × 1.
// Precondició: n ≥ 0.

int factorial(int n);



// potencia(a,b) retorna a^b.
// Precondició: b ≥ 0.
// Nota: Es considera que 0^0 val 1.

int potencia(int a, int b);

